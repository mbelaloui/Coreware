#include "../../../inc/vm.h"

void		visu_pause(t_vm *vm)
{
	int		c;

	mvprintw(65, 226, "\tPAUSE");
	while (1)
	{
		c = getch();
		if (c == 32 || c == '=')
			break ;
		if (c == 27)
			esc_visu(vm, 0);
	}
	mvprintw(65, 226, "\t     ");
}
