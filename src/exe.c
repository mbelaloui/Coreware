#include "./libft/inc/libft.h"

int		main(void)
{

	int nbr = 0;
	unsigned char *tab;

	ft_printf("/******************************************************** \\ \n");

	while (nbr <= MAX_INT)
	{
		ft_printf(" nbr = [%d]\n", nbr);
		tab = ft_int_to_byts(nbr, 4);
		ft_printf("[%d] [%d] [%d] [%d]\n", tab[0] ,tab[1] ,tab[2], tab[3] );

		ft_printf("/******************************************************** \\ \n");
		ft_printf("[%d]\n", ft_byts_to_int(tab));

		nbr+= 100000000;
		ft_temporize(1);
		ft_clear_scr();
	}
	return (0);
}
